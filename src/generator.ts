/**
 * Build all inputs for the AddData circuit (mint mode).
 * Pass the result directly to snarkjs.groth16.fullProve().
 */
export async function buildMintInput(
  rawPrivateKey: Uint8Array | string,
  message: FieldElement[/* ${N} */],
  nonce: FieldElement
): Promise<MintInput> {
  const myPubKey = await derivePublicKey(rawPrivateKey);
  const bjj = await babyJub();
  const scalar = deriveSecretScalar(rawPrivateKey);

  // Self-encrypt: ECDH with own public key
  const sharedKey = await computeSharedKey(rawPrivateKey, myPubKey);
  const ciphertext = await poseidonEncrypt(message, sharedKey, nonce);

  // For mint, "old" ciphertext is a dummy (same as new — the circuit skips the check when mode=0)
  const zeroKey: PublicKey = [0n, 1n]; // additive identity on Baby Jubjub y-axis
  const zeroCipher = await poseidonEncrypt(message, sharedKey, nonce);

  return {
    myPrivateKey: scalar.toString(),
    oldSenderPublicKey: [myPubKey[0].toString(), myPubKey[1].toString()],
    newResultKey: [sharedKey[0].toString(), sharedKey[1].toString()],
    oldResultKey: [sharedKey[0].toString(), sharedKey[1].toString()],
    newMessage: message.map(String),
    oldMessage: message.map(String),
    oldNonce: nonce.toString(),
    newReciverPublicKey: [myPubKey[0].toString(), myPubKey[1].toString()],
    newNonce: nonce.toString(),
    newComputedCipherText: ciphertext.map(String),
    oldComputedCipherText: zeroCipher.map(String),
    myPublicKey: [myPubKey[0].toString(), myPubKey[1].toString()],
    enableOneValueCheck: '0',
  };
}

